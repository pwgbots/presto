from django.contrib import admin
from django.urls import include, re_path
from django.conf import settings

if settings.USE_SAML is True:

    from daeauth import AdminSiteWithExternalAuth
    admin.site = AdminSiteWithExternalAuth()
    admin.autodiscover()
    
    urlpatterns = [
        re_path(r'^', include('presto.urls')),
        re_path(r'^admin/', admin.site.urls),
        re_path(r'^saml2/', include('djangosaml2.urls')), 
    ]

else:
    urlpatterns = [
        re_path(r'^presto/', include('presto.urls')),
        re_path(r'^admin/', admin.site.urls),
    ]
